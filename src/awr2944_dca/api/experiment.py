"""Python API for managing experiments."""

from __future__ import annotations

import os
import sys
from pathlib import Path
from typing import Any

import typer
import yaml

from awr2944_dca.config.derived import compute_derived
from awr2944_dca.config.schema import RadarConfig
from awr2944_dca.config.validation import ValidationResult, validate_config
from awr2944_dca.context import discover_context
from awr2944_dca.ti.probe import find_studio, generate_probe_script


class Experiment:
    """Core API object for an AWR2944 DCA experiment."""

    def __init__(self, root_dir: Path, config_path: Path):
        self.root_dir = root_dir
        self.config_path = config_path

    @classmethod
    def init(cls, name: str, preset: str = "first-capture", root: str | Path = ".") -> Experiment:
        """Scaffold a new experiment."""
        from awr2944_dca.config.presets import get_preset

        root_path = Path(root).resolve()
        exp_dir = root_path / name

        if exp_dir.exists():
            raise FileExistsError(f"Experiment directory already exists: {exp_dir}")

        exp_dir.mkdir(parents=True)
        (exp_dir / ".awr-experiment").touch()

        dirs = [
            exp_dir / "raw",
            exp_dir / "ti_config",
            exp_dir / "screenshots",
            exp_dir / "compare_layouts",
            exp_dir / "outputs",
            exp_dir / "ti",
        ]
        for d in dirs:
            d.mkdir(parents=True, exist_ok=True)

        config_path = exp_dir / "capture.yaml"
        preset_dict = get_preset(preset)
        preset_dict["experiment"]["name"] = name
        
        # Keep it compatible with schema, preserving the warning logic if needed
        header = f"# Generated by awr2944_dca from preset: {preset}\n# Review and customize before use.\n\n"
        yaml_str = yaml.dump(preset_dict, default_flow_style=False, sort_keys=False)
        config_path.write_text(header + yaml_str, encoding="utf-8")

        import datetime
        from awr2944_dca import __version__
        
        manifest = {
            "experiment": name,
            "preset": preset,
            "created": datetime.datetime.now(datetime.timezone.utc).isoformat(),
            "tool_version": __version__,
            "status": "initialized",
            "notes": "",
        }
        (exp_dir / "manifest.yaml").write_text(yaml.dump(manifest, default_flow_style=False, sort_keys=False), encoding="utf-8")
        
        notes_path = exp_dir / "notes.md"
        notes_path.write_text(
            f"# {name} Notes\n\n"
            f"## Setup Notes\n\n"
            f"- Preset: {preset}\n"
            f"- Date: \n"
            f"- Operator: \n\n"
            f"## Observations\n\n"
            f"## Issues\n\n",
            encoding="utf-8",
        )

        return cls(root_dir=exp_dir, config_path=config_path)

    @classmethod
    def open(cls, path: str | Path = ".") -> Experiment:
        """Open an existing experiment by discovering context."""
        ctx = discover_context(Path(path))
        if not ctx:
            raise FileNotFoundError(f"No .awr-experiment found near {path}")
        return cls(root_dir=ctx.root_dir, config_path=ctx.config_path)

    def get_config(self) -> RadarConfig:
        """Load and return the current RadarConfig."""
        return RadarConfig.from_yaml(self.config_path)

    def check(self) -> list[ValidationResult]:
        """Validate the config against device specs."""
        cfg = self.get_config()
        return validate_config(cfg)

    def summary(self) -> dict[str, Any]:
        """Return structured summary data (can be printed by CLI)."""
        cfg = self.get_config()
        derived = compute_derived(cfg)
        
        expected_bytes = (
            cfg.adc.samples_per_chirp
            * cfg.hardware.num_rx
            * cfg.frame.chirps_per_frame
            * cfg.frame.num_frames
            * cfg.adc.bytes_per_sample_per_rx
        )
        
        # Return a dictionary of the information needed to render the summary
        return {
            "config": cfg,
            "derived": derived,
            "expected_bytes": expected_bytes,
        }

    def _update_yaml_field(self, section: str, key: str, value: str) -> None:
        """Helper to modify capture.yaml directly without losing all formatting."""
        data = yaml.safe_load(self.config_path.read_text(encoding="utf-8"))
        if section not in data:
            data[section] = {}
        data[section][key] = value
        self.config_path.write_text(yaml.dump(data, default_flow_style=False, sort_keys=False), encoding="utf-8")

    def set_device(self, device: str) -> None:
        """Set the radar device name."""
        self._update_yaml_field("rig", "radar", device)

    def set_capture(self, backend: str) -> None:
        """Set the capture backend name."""
        self._update_yaml_field("rig", "capture_card", backend)

    def config_edit(self, dry_run: bool = False) -> None:
        """Open the system editor for capture.yaml."""
        if not dry_run:
            editor = os.environ.get("VISUAL") or os.environ.get("EDITOR")
            if sys.platform == "win32" and not editor:
                os.system(f"notepad {self.config_path}")
            else:
                typer.launch(str(self.config_path))

    def find_studio(self) -> list:
        """Find TI mmWave Studio installations."""
        return find_studio()

    def generate_ti_probe(self) -> Path:
        """Generate the offline probe script, save manifest, and return its path."""
        import uuid
        import json
        
        log_dir = self.root_dir / "ti" / "probe_logs"
        log_dir.mkdir(parents=True, exist_ok=True)
        probe_lua = log_dir / "probe.lua"
        
        probe_id = str(uuid.uuid4())
        manifest = {"probe_id": probe_id}
        (log_dir / "probe_manifest.json").write_text(json.dumps(manifest, indent=2))
        
        return generate_probe_script(probe_lua, probe_id=probe_id)

    def generate_ti_inventory(self) -> Path:
        """Generate the offline API inventory extraction script."""
        import uuid
        import json
        from awr2944_dca.ti.probe import generate_inventory_script
        
        probe_id = str(uuid.uuid4())
        
        log_dir = self.root_dir / "ti" / "probe_logs"
        log_dir.mkdir(parents=True, exist_ok=True)
        
        manifest = log_dir / "inventory_manifest.json"
        manifest.write_text(json.dumps({"probe_id": probe_id}), encoding="utf-8")
        
        script = log_dir / "inventory.lua"
        generate_inventory_script(script, probe_id)
        
        return script

    def generate_ti_connection_probe(self, com_num: int, baud: int = 921600, timeout_ms: int = 1000) -> Path:
        """Generate the connection-only probe script."""
        import uuid
        import json
        from awr2944_dca.ti.probe import generate_connection_probe_script
        
        run_id = str(uuid.uuid4())
        
        log_dir = self.root_dir / "ti" / "probe_logs"
        log_dir.mkdir(parents=True, exist_ok=True)
        
        manifest = log_dir / "connection_manifest.json"
        manifest.write_text(json.dumps({"run_id": run_id}), encoding="utf-8")
        
        script = log_dir / "connection_probe.lua"
        generate_connection_probe_script(script, run_id, com_num, baud, timeout_ms)
        
        return script

